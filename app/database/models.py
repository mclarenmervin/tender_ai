class User(Base):
    __tablename__='users'
    id=Column(Integer,primary_key=True,index=True)
    name=Column(String(150),nullable=False)
    email=Column(String(255),unique=True,index=True,nullable=False)
    password_hash=Column(Text,nullable=False)
    role=Column(String(50),default='buyer')
    is_active=Column(Boolean,default=True)
    created_at=Column(DateTime(timezone=True),server_default=func.now())
class Tender(Base):
    __tablename__='tenders'
    id=Column(Integer,primary_key=True,index=True)
    user_id=Column(Integer,ForeignKey('users.id'),index=True)
    source=Column(String(100),nullable=False)
    tender_id=Column(String(255),nullable=False)
    title=Column(Text,nullable=False)
    department=Column(Text); state=Column(String(100)); estimated_value=Column(BigInteger); deadline=Column(Date); url=Column(Text); description=Column(Text); category=Column(String(255))
    relevance_score=Column(Float); ai_recommendation=Column(Boolean); ai_reason=Column(Text)
    status=Column(String(100),default='new')
    created_at=Column(DateTime(timezone=True),server_default=func.now())
    updated_at=Column(DateTime(timezone=True),server_default=func.now(),onupdate=func.now())
    tracking=relationship('TenderTracking',back_populates='tender',uselist=False)
    eligibility=relationship('TenderEligibility',uselist=False)
    bid_decision=relationship('BidDecision',uselist=False)
    __table_args__=(UniqueConstraint('user_id','source','tender_id',name='uq_user_source_tender_id'),)
